    private void loginEntrarCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginEntrarCadastrarActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here: // criar um objeto da classe LoginController
        LoginController controller = new LoginController();
        // criando variavel que recebe o retorno do metodo autenticar
        Usuario logou = controller.authenticate(
                campoUsuario.getText(), campoSenha.getText());
        if(logou != null){
           JOptionPane.showMessageDialog(null,"Usu치rio logado com sucesso! "+ logou.getNome());
           // abrir a tela Home
           TelaPrincipal telahome = new TelaPrincipal(logou);
           telahome.setVisible(true);
          // fechando a tela de Login 
          dispose();
      
        }else{
            JOptionPane.showMessageDialog(null,"Usu치rio e Senha Incorreta!"); 
        }// fim do else
        mudarImagem();
    }//GEN-LAST:event_loginEntrarCadastrarActionPerformed

